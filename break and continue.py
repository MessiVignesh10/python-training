# while loop break and continue statements
i=0
while i<10:
    print(i)
    if i==5:
       break
    i=i+1
print("__________________________________________")
j=-1
while j<10:
        j=j+1
        if j==5:
            continue
        print(j)
k=0
while k<10:
    print("________")
    k=k+1
    
